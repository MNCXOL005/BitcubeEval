
import java.util.*;

class Person{
	
	// instance variables
	protected String forename;  // firstname
	protected String surname;    //surname
	protected String email;      // email
	protected String DateOfBirth;  // date of birth
	
	public Person (String forename, String surname, String email, String DateOfBirth){

		this.forename = forename;
		this.surname = surname;
		this.email = email;
		this.DateOfBirth = DateOfBirth;
		
	}
	
}
